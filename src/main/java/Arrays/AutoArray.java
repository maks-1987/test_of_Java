package Arrays;

public class AutoArray {
    public static void main(String[] args) {
        // инициализация массива {...};
        int[] mount_days = {31,28,31,30,31,30,
                            31,31,30,31,30,31};
        // то же выведем элемент под индексом 3
        System.out.println("В апреле " + mount_days[3] + " дней");
    }
}
